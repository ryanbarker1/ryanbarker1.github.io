			    Standards and Specifications</span>.  No one story buildings shall be constructed on Lots with a fully enclosed floor area of less than seven hundred square feet (700 Ft.<sup>2</sup>), exclusive of carport, garage and open porches.  No two-story buildings, or higher, shall be constructed with a fully enclosed first floor area of less than five hundred square feet (500 Ft.<sup>2</sup>).  No one and one-half story building shall be constructed with a fully enclosed first floor area of less than six hundred square feet (600 Ft.<sup>2</sup>).  The above requirements may be modified by the Board when in its judgement the aesthetic and property values in the area will be maintained even though a structure size may be smaller.</td>
